export const getArrayUpper = () => {
    let arrayAscii = []
    for(let i = 65; i < 91; i++)
        arrayAscii.push(String.fromCharCode(i))

    return arrayAscii;
}

export const getArrayLower = () => {
    let arrayAscii = []
    for(let i = 65; i < 91; i++)
        arrayAscii.push(String.fromCharCode(i))

    return arrayAscii;
}